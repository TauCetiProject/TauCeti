#!/usr/bin/env bash
# mk_all.sh — keep the root aggregator `TauCeti.lean` importing every module under
# `TauCeti/`, in sorted order. This is the same job as Mathlib's `lake exe mk_all`,
# but a tiny self-contained script: it preserves our doc header and needs no build.
#
#   Scripts/mk_all.sh [--check] [root]
#
#   (default)  rewrite <root>/TauCeti.lean in place so it is canonical.
#   --check    exit 1 (printing a diff) if <root>/TauCeti.lean is not already canonical.
#   root       repository root to operate on (default: the current directory).
#
# CI (.github/workflows/mk-all.yml) runs this with the TRUSTED base copy of the script
# over an untrusted PR checkout, so it is defensive: it lists filenames only (never reads
# or runs module code), rejects pathological paths, and never writes through a symlink.
set -euo pipefail

CHECK=0
case "${1:-}" in --check) CHECK=1; shift;; esac
ROOT="${1:-.}"
TARGET="$ROOT/TauCeti.lean"

# Build the canonical file: a fixed doc header, then `import` lines for every module
# under TauCeti/, byte-sorted (LC_ALL=C) for a stable, locale-independent result.
# NUL-delimited enumeration + a strict allowlist defends against newline/metacharacter
# filenames in an untrusted checkout (a valid Lean module path is identifier-ish only).
gen() {
  cat <<'EOF'
-- This is the AI-owned root of the TauCeti mathematics library.
--
-- Everything reachable from here must be sorry-free, and must not import
-- `TauCetiRoadmap` or `TauCetiReview` (both enforced in CI).
--
-- This import list is generated by `Scripts/mk_all.sh` (every `TauCeti/` module,
-- sorted); CI keeps it in sync. Do not edit it by hand.
EOF
  local f mods=()
  while IFS= read -r -d '' f; do
    case "$f" in
      *..* | *[!A-Za-z0-9._/]* )
        echo "mk_all: refusing unsafe module path: $f" >&2; return 1 ;;
    esac
    mods+=("$f")
  done < <(cd "$ROOT" && find TauCeti -type f -name '*.lean' -print0)
  [ "${#mods[@]}" -gt 0 ] || { echo "mk_all: no modules found under $ROOT/TauCeti" >&2; return 1; }
  printf '%s\n' "${mods[@]}" | sed -e 's#\.lean$##' -e 's#/#.#g' | LC_ALL=C sort | sed 's#^#import #'
}

if [ "$CHECK" = 1 ]; then
  tmp="$(mktemp)"; trap 'rm -f "$tmp"' EXIT
  gen > "$tmp"
  if diff -u "$TARGET" "$tmp"; then
    echo "TauCeti.lean is up to date."
  else
    echo "::error::TauCeti.lean is out of date — run \`Scripts/mk_all.sh\` to regenerate it"
    exit 1
  fi
else
  # Never write *through* a symlink (an untrusted checkout could make TauCeti.lean one).
  if [ -L "$TARGET" ]; then echo "::error::$TARGET is a symlink — refusing to write" >&2; exit 1; fi
  tmp="$(mktemp "$ROOT/.mk_all.XXXXXX")"; trap 'rm -f "$tmp"' EXIT
  gen > "$tmp"
  mv -f "$tmp" "$TARGET"   # replaces the file by rename; never follows a symlink
  trap - EXIT
  echo "wrote $TARGET"
fi
